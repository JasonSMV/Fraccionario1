package com.company;

public class Fecha {

    private String año;
    private String mes;
    private String dia;

    public String getAño() {
        return año;
    }

    public String getMes() {
        return mes;
    }

    public String getDia() {
        return dia;
    }

    public void setAño(String año) {
        this.año = año;
    }

    public void setMes(String mes) {
        this.mes = mes;
    }

    public void setDia(String dia) {
        this.dia = dia;
    }
}
